function basicOp(operation, value1, value2)
{
  
  const operator = operation[0];
  
  let total = 0;
  
  
    if(operator === '/')
      return total = value1 / value2;
    if(operator === '+')
      return total = value1 + value2;
    if(operator === '-')
      return total = value1 - value2;
    if(operator === '*')
     return total = value1 * value2;
    

}